# Describes and performs escape sequences
# \n: new line - adds string after \n to new line
str1 = "Basics of \n python programming language"
print(str1)

# \t: Horizontal tab - adds tab in string horizontally
str2 = "Hello this is \t python"
print(str2)

# \\: Backslash - adds single backslash in the string
str3 = "Welcome to the \\ course"
print(str3)

# \": add double quotes in the string
str4 = "He said to me \"Hello\""
print(str4)

# \': add single quote in the string
str5 = "It\'s Holiday today"
print(str5)

# \u: represent unicode character- adds character to string using unicode
str6 = "This is division sign: \u00F7"
print(str6)

# \r: carriage return - prints string present after \r
str7 = "hello world \r this is python"
print(str7)

