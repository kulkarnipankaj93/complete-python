# Taking input from user and performing arithmatic operations

# Taking Input
a = input("Enter Value for a:")
print(a)
a = int(a)

b = input("Enter Value for b:")
print(b)
b = int(b)


# Arithmetic operations

# Addition
add = a+b
print("Addition of two is ", add)

# Subtraction
sub = a-b
print("Subtraction of two is", sub)

# Multiplication
mul = a*b
print("Multiplication of two is", mul)

# Division
div = a/b
print("Division of two is", div)

# Floor-Division
fdiv = a//b
print("Floor Division of two is", fdiv)

# Modulus operator
mod = a%b
print("Remainder of the division is", mod)

