# Membership Operator
Mobiles = ['Oneplus', 'Iphone', 'Samsung', 'Redmi']
value = 'Samsung'

# in operator
result5 = value in Mobiles
print(result5)

# not in operator
result6 = value not in Mobiles
print(result6)

